package com.contact.CustomHelperClasses;

public class Pair {
	boolean first;
	boolean second;
	
	
	public Pair() {
		super();
	}
	
	public Pair(boolean first, boolean second) {
		super();
		this.first = first;
		this.second = second;
	}
	public boolean isFirst() {
		return first;
	}
	public void setFirst(boolean first) {
		this.first = first;
	}
	public boolean isSecond() {
		return second;
	}
	public void setSecond(boolean second) {
		this.second = second;
	}
	
	
}
